// 统一读取静态 UI 文案。缺少某个语言时先回退中文，避免切换语言后出现空白。
function t(key) {
  return translations[currentLanguage]?.[key] || translations.zh[key] || key;
}

// API 的可展示字段统一采用 titleI18n、summaryI18n、ticketNoteI18n 这类三语言结构。
// 前端只按当前语言取值，不在页面层手写业务文案，方便后续接后台管理系统。
function localizedText(item, field) {
  const i18n = item[`${field}I18n`];
  return i18n?.[currentLanguage] || i18n?.zh || item[field] || "";
}

// purchaseText 这种字段本身就是三语言 map，不再拼 I18n 后缀。
function localizedMap(value) {
  if (!value || typeof value !== "object") {
    return "";
  }
  return value[currentLanguage] || value.zh || "";
}

function formText(payload, key) {
  return typeof payload[key] === "string" ? payload[key].trim() : "";
}

function connectTopicLabel(topic) {
  const labels = {
    ticket: t("topicTicket"),
    "cd-select": t("topicCDSelect"),
    support: t("topicSupport"),
    collab: t("topicCollab"),
  };
  return labels[topic] || topic;
}

// Connect 表单先打开 mailto 草稿，让用户可以直接通过自己的邮件 App 或网页邮箱发送。
// 同时保留本地 API 入库，后续如果接服务端邮件、客服后台或管理列表，不需要再改页面入口。
function buildConnectMailto(payload) {
  const nickname = formText(payload, "nickname") || "-";
  const email = formText(payload, "email") || "-";
  const topic = connectTopicLabel(formText(payload, "topic"));
  const message = formText(payload, "message") || "-";
  const subject = `[LIVE LIFE CONNECT] ${topic} / ${nickname}`;
  const body = [
    "LIVE LIFE CONNECT",
    "",
    `${t("labelNickname")}: ${nickname}`,
    `${t("labelEmail")}: ${email}`,
    `${t("labelTopic")}: ${topic}`,
    "",
    `${t("labelMessage")}:`,
    message,
    "",
    `Page: ${window.location.href}`,
    `Time: ${new Date().toLocaleString()}`,
  ].join("\n");

  return `mailto:${connectMailAddress}?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(body)}`;
}

function openConnectMail(payload) {
  const mailtoUrl = buildConnectMailto(payload);
  const link = document.createElement("a");
  link.href = mailtoUrl;
  link.target = "_blank";
  link.rel = "noopener noreferrer";
  document.body.appendChild(link);
  link.click();
  link.remove();
}

function escapeHTML(value) {
  return String(value || "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

async function getJSON(url) {
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`${t("requestFailed")}: ${response.status}`);
  }
  return response.json();
}

// V2/V3 只是页面视觉方案，后端 API、数据结构、业务逻辑保持同一套。
// 这样将来给客户 review 时，可以在右上角切换方案，不需要开分支或维护多套接口。
function readInitialDesign() {
  const params = new URLSearchParams(window.location.search);
  const fromUrl = params.get("design");
  if (fromUrl === "v2" || fromUrl === "v2-refined" || fromUrl === "v3") {
    return fromUrl;
  }
  try {
    const stored = window.localStorage.getItem("liveLifeDesignVariant");
    return stored === "v3" || stored === "v2-refined" ? stored : "v2";
  } catch {
    return "v2";
  }
}

function applyDesign(design, options = { updateUrl: true }) {
  currentDesign = design === "v3" || design === "v2-refined" ? design : "v2";
  document.body.dataset.design = currentDesign;
  if (designSelect) {
    designSelect.value = currentDesign;
  }
  try {
    window.localStorage.setItem("liveLifeDesignVariant", currentDesign);
  } catch {
    // 本地预览里 localStorage 不可用也不影响展示。
  }
  if (options.updateUrl) {
    const url = new URL(window.location.href);
    url.searchParams.set("design", currentDesign);
    url.searchParams.set("v", "20260611-v2v3-logo-mail");
    window.history.replaceState({}, "", url);
  }
}

// 切换语言时，静态文案、placeholder、演出卡片、CD 卡片、Archive 和 Schedule 都要重渲染。
function applyLanguage(language) {
  currentLanguage = translations[language] ? language : "zh";
  document.documentElement.lang = t("langAttr");

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.setAttribute("placeholder", t(node.dataset.i18nPlaceholder));
  });

  languageButtons.forEach((button) => {
    const active = button.dataset.lang === currentLanguage;
    button.classList.toggle("active", active);
    button.setAttribute("aria-pressed", String(active));
  });

  healthStatus.textContent = lastHealth?.ok ? t("healthOnline") : lastHealth ? t("healthOffline") : t("healthChecking");

  renderSchedule();
  renderEvents(cachedEvents);
  renderCatalog(cachedCatalogItems);
  renderContents(cachedContents);
}

function renderSchedule() {
  const liveItems = cachedEvents.filter((event) => event.owned).slice(0, 2).map((event) => ({
    type: t("scheduleLive"),
    date: event.date,
    title: localizedText(event, "title"),
    meta: event.venue,
    status: localizedText(event, "ticketNote") || t("ticketPending"),
  }));

  const catalogItems = cachedCatalogItems.slice(0, 2).map((item) => ({
    type: item.format === "vinyl" ? t("scheduleVinyl") : t("scheduleCD"),
    date: "TBD",
    title: localizedText(item, "title"),
    meta: item.artist,
    status: item.status || item.price || "TBD",
  }));

  const items = [...liveItems, ...catalogItems];
  scheduleList.innerHTML = items.map((item) => `
    <article class="schedule-item">
      <div class="schedule-date">${escapeHTML(item.date)}</div>
      <div>
        <span>${escapeHTML(item.type)}</span>
        <strong>${escapeHTML(item.title)}</strong>
        <small>${escapeHTML(item.meta)} / ${escapeHTML(item.status)}</small>
      </div>
    </article>
  `).join("");
}

function renderEvents(events) {
  const own = events.filter((event) => event.owned);
  const rest = events.filter((event) => !event.owned);
  ownedEvents.innerHTML = own.map((event) => renderEvent(event, true)).join("");
  recommendedEvents.innerHTML = rest.map((event) => renderEvent(event, false)).join("");
}

function renderEvent(event, featured) {
  const tags = (event.tags || []).map((tag) => `<span class="tag">${escapeHTML(tag)}</span>`).join("");
  const lineup = (event.lineup || []).map((name) => `<span>${escapeHTML(name)}</span>`).join("");
  const image = event.imageUrl
    ? `<img class="event-image" src="${escapeHTML(event.imageUrl)}" alt="${escapeHTML(localizedText(event, "title"))}" />`
    : `<div class="event-image placeholder" aria-hidden="true"></div>`;
  const badge = featured ? t("ownBadge") : t("recommendBadge");
  const ticketNote = localizedText(event, "ticketNote") || t("ticketPending");
  const sourceNote = localizedText(event, "sourceNote");

  return `
    <article class="event-card ${featured ? "featured" : ""}">
      ${image}
      <div class="event-body">
        <span class="pill">${escapeHTML(badge)}</span>
        <h3>${escapeHTML(localizedText(event, "title"))}</h3>
        <div class="meta">
          <span>${escapeHTML(event.date)}</span>
          <span>${escapeHTML(event.time)}</span>
          <span>${escapeHTML(event.venue)}</span>
          <span>${escapeHTML(event.price)}</span>
        </div>
        <p class="summary">${escapeHTML(localizedText(event, "summary"))}</p>
        <div class="lineup">${lineup}</div>
        <p class="note"><strong>${escapeHTML(t("ticketPending"))}</strong> ${escapeHTML(ticketNote)}</p>
        ${sourceNote ? `<p class="source-note"><strong>${escapeHTML(t("sourceNote"))}</strong> ${escapeHTML(sourceNote)}</p>` : ""}
        <div class="tags">${tags}</div>
      </div>
    </article>
  `;
}

function renderCatalog(items) {
  const filtered = currentFormat === "all" ? items : items.filter((item) => item.format === currentFormat);
  cdList.innerHTML = filtered.map((item) => `
    <article class="catalog-card">
      ${item.imageUrl ? `<img src="${escapeHTML(item.imageUrl)}" alt="${escapeHTML(localizedText(item, "title"))}" />` : `<div class="catalog-art" aria-hidden="true"></div>`}
      <div class="catalog-body">
        <span class="pill">${escapeHTML(item.format === "vinyl" ? t("formatVinyl") : t("formatCD"))}</span>
        <h3>${escapeHTML(localizedText(item, "title"))}</h3>
        <p class="artist">${escapeHTML(item.artist)}</p>
        <p>${escapeHTML(localizedText(item, "summary"))}</p>
        <div class="track-list">${(item.tracks || []).map((track) => `<span>${escapeHTML(track)}</span>`).join("")}</div>
        <div class="catalog-actions">
          <span>${escapeHTML(item.price || "TBD")}</span>
          <a class="button buy-button" href="${escapeHTML(item.purchaseUrl)}" target="_blank" rel="noreferrer">${escapeHTML(localizedMap(item.purchaseText) || t("viewDetail"))}</a>
        </div>
        <small>${escapeHTML(t("externalShopNote"))}</small>
      </div>
    </article>
  `).join("");
}

function renderContents(contents) {
  contentList.innerHTML = contents.map((item) => `
    <article class="content-card">
      <span class="pill">${escapeHTML(item.type)}</span>
      <h3>${escapeHTML(localizedText(item, "title"))}</h3>
      <p>${escapeHTML(localizedText(item, "summary"))}</p>
    </article>
  `).join("");
}

// 页面启动时，先应用默认中文和设计方案，再并行加载健康检查、演出、CD 严选和 Archive。
async function boot() {
  applyDesign(currentDesign, { updateUrl: false });
  applyLanguage(currentLanguage);

  try {
    const health = await getJSON("/api/health");
    lastHealth = { ok: true, value: health };
    healthStatus.textContent = t("healthOnline");
    healthMeta.textContent = `${health.brand} / ${health.service}`;
    statusDot.classList.add("ok");
  } catch (error) {
    lastHealth = { ok: false, value: error };
    healthStatus.textContent = t("healthOffline");
    healthMeta.textContent = error.message;
  }

  try {
    const [eventsData, cdData, contentsData] = await Promise.all([
      getJSON("/api/events"),
      getJSON("/api/cd-items"),
      getJSON("/api/contents"),
    ]);
    cachedEvents = eventsData.events || [];
    cachedCatalogItems = cdData.items || [];
    cachedContents = contentsData.contents || [];
    renderSchedule();
    renderEvents(cachedEvents);
    renderCatalog(cachedCatalogItems);
    renderContents(cachedContents);
  } catch (error) {
    scheduleList.innerHTML = `<p class="summary">${escapeHTML(error.message)}</p>`;
  }
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

designSelect.addEventListener("change", () => {
  applyDesign(designSelect.value);
});

formatTabs.forEach((button) => {
  button.addEventListener("click", () => {
    currentFormat = button.dataset.format;
    formatTabs.forEach((tab) => tab.classList.toggle("active", tab === button));
    renderCatalog(cachedCatalogItems);
  });
});

// Connect 表单现在先写入本地 SQLite，后续可以在后端继续接邮件通知、客服系统或后台管理列表。
connectForm.addEventListener("submit", async (event) => {
  event.preventDefault();
  formResult.textContent = t("submitting");

  const data = Object.fromEntries(new FormData(connectForm).entries());
  openConnectMail(data);

  try {
    const response = await fetch("/api/connect", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(data),
    });
    const result = await response.json();
    if (!response.ok) {
      throw new Error(result.error || t("submitFailed"));
    }
    formResult.textContent = result.message || t("submitFallback");
    connectForm.reset();
  } catch (error) {
    formResult.textContent = error.message;
  }
});

boot();
